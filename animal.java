package com.davinci.helloworld;

public class animal {
    private String name;
    private String color;
    private int legs;
    private boolean hasTale;

    public animal(String name, String color, int legs, boolean hasTale) {
        this.name = name;
        this.color = color;
        this.legs = legs;
        this.hasTale = hasTale;
    }
}
